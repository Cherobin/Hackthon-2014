package com.github.cherobin.hackthon_2014.empresa;

public class Company {

	String nome;
	String cidade;
	String estado;
	String sobre;
	String telefone;
	String email;

	public Company(String nome, String cidade, String estado, String sobre,
			String telefone, String email) { 
		this.nome = nome;
		this.cidade = cidade;
		this.estado = estado;
		this.sobre = sobre;
		this.telefone = telefone;
		this.email = email;
	}

}
