package com.github.cherobin.hackthon_2014.VO;

public class Company {

	public String nome;
	public String cidade;
	public String estado;
	public String sobre;
	public String telefone;
	public String email;

	public Company(String nome, String cidade, String estado, String sobre,
			String telefone, String email) { 
		this.nome = nome;
		this.cidade = cidade;
		this.estado = estado;
		this.sobre = sobre;
		this.telefone = telefone;
		this.email = email;
	}

}
